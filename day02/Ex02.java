class Ex02{

  public static void main(String[] args){

	// 10진수 정수타입
	// byte(1byte), short(2byte)
	//, int(4byte), long(8byte)
	byte su1;
	su1=127;
	System.out.println(su1);
	short su2;
	su2=32767;
	System.out.println(su2);
	int su3;
	su3=2147483647;
	System.out.println(su3);
	long su4;
	su4=2147483648L;
	System.out.println(su4);
  }

}